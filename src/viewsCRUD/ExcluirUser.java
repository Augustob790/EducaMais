    public void ExcluirUsuario() {
        UsuarioController controle = new UsuarioController();
        ArrayList<Usuario> vetorDeUsuarios = controle.buscarUsuario();
        for (Usuario u : vetorDeUsuarios) {
            modelo.addElement(u);
        }
    }

