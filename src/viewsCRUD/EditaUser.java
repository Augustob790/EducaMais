    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        UsuarioController controle = new UsuarioController();
        Usuario usuario = controle.procuraUsuario(nomepassed.getText());
        if (usuario != null) {
            jTextFieldNome.setText(usuario.getNome());
            jTextFieldlTipo.setText(usuario.getTipo());
            jTextFieldTelefone.setText(usuario.getTelefone());
            jTextFieldSenha.setText(usuario.getId() + "");
            JOptionPane.showMessageDialog(null, "Usuário Confirmado!!");
        } else {
            JOptionPane.showMessageDialog(null, "Usuário não Confirmado!!");
        }
    }//GEN-LAST:event_searchActionPerformed

