    private void confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarActionPerformed
        UsuarioController controle = new UsuarioController();
        int idUser = Integer.parseInt(jTextFieldId.getText());
        String senhaUser = ((Usuario) modelo.getSelectedItem()).getSenha();
        controle.AlterarUsuario(idUser, jTextFieldNome.getText(), jTextFieldlTipo.getText(), jTextFieldTelefone.getText(), senhaUser);

    }//GEN-LAST:event_confirmarActionPerformed

