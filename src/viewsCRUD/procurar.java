        Usuario usuario = controle.procuraUsuario(nomepassed.getText());
        if (usuario != null) {
            jTextFieldNome.setText(usuario.getNome());

            jTextFieldTipo.setText(usuario.getTipo());
            jTextFieldTelefone.setText(usuario.getTelefone());
            jTextFieldId.setText(usuario.getId() + "");
            JOptionPane.showMessageDialog(null, "Usu√°rio Confirmado!!");
        } else {
            JOptionPane.showMessageDialog(null, "Usu√°rio n√£o Confirmado!!");
        }
